async function generateWorksheetPDF(worksheetText, teacherName, outputPath) {
  let browser;
  try {
    const puppeteer = require('puppeteer');
    const parsed    = parseWorksheet(worksheetText);
    const pages     = paginate(parsed.activities);

    let logo64 = '';
    const lp = path.join(__dirname, '..', 'assets', 'logo.jpeg');
    if (fs.existsSync(lp)) logo64 = fs.readFileSync(lp).toString('base64');

    let pagesHtml = '';
    for (let i=0;i<pages.length;i++) {
      pagesHtml += `<div class="page">${buildSvg(pages[i], parsed, teacherName, i+1, pages.length, logo64)}</div>`;
    }

    const html = `<!DOCTYPE html><html><head><meta charset="UTF-8"/>
<style>*{margin:0;padding:0;box-sizing:border-box;}body{background:white;}
.page{width:794px;height:1123px;overflow:hidden;page-break-after:always;}
.page:last-child{page-break-after:avoid;}svg{display:block;}</style>
</head><body>${pagesHtml}</body></html>`;

    const tmp = path.join(os.tmpdir(),'ws_'+Date.now()+'.html');
    fs.writeFileSync(tmp, html);

    browser = await puppeteer.launch({ headless:true, args:['--no-sandbox','--disable-setuid-sandbox'] });
    const pg = await browser.newPage();
    await pg.goto('file:///'+tmp.replace(/\\/g,'/'), { waitUntil:'networkidle0' });
    await pg.pdf({ path:outputPath, width:'794px', height:'1123px', printBackground:true, margin:{top:0,right:0,bottom:0,left:0} });
    fs.unlinkSync(tmp);
    return outputPath;
  } finally {
    if (browser) await browser.close();
  }
}

async function generateWorksheetImage(worksheetText, teacherName, outputPath) {
  const sharp  = require('sharp');
  const parsed = parseWorksheet(worksheetText);
  const pages  = paginate(parsed.activities);
  let logo64='';
  const lp = path.join(__dirname, '..', 'assets', 'logo.jpeg');
  if (fs.existsSync(lp)) logo64 = fs.readFileSync(lp).toString('base64');
  const svg = buildSvg(pages[0]||{acts:[],isFirst:true}, parsed, teacherName, 1, pages.length, logo64);
  await sharp(Buffer.from(svg)).png({ quality:95 }).toFile(outputPath);
  return outputPath;
}

module.exports = { generateWorksheetPDF, generateWorksheetImage };
