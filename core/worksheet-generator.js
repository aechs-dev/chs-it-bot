// ── Helpers ──────────────────────────────────────────────────────────────────
function esc(s) {
  return String(s||'').replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;').replace(/'/g,'&apos;');
}

function wrap(text, maxCh) {
  const words = String(text||'').split(' ');
  const lines = []; let cur = '';
  for (const w of words) {
    if ((cur+' '+w).trim().length > maxCh) { if (cur) lines.push(cur.trim()); cur = w; }
    else cur = (cur+' '+w).trim();
  }
  if (cur) lines.push(cur.trim());
  return lines.length ? lines : [''];
}

// ── Parser ───────────────────────────────────────────────────────────────────
function isJunk(l) {
  return /^(here'?s?|i'?ve?|sure|let me|note:|_\(chs|ready for print|answer key is sep|what grade|i can adjust|generated by|academic year|student name:|class.*grade:|subject:|score:|prepared by:)/i.test(l)
    || /CHS\.ai is still in development/i.test(l)
    || /^\s*$/.test(l);
}

function parseWorksheet(raw) {
  const lines = raw.split('\n')
    .map(l => l.replace(/\*\*/g,'').replace(/\*/g,'').replace(/^#+\s*/,'').trim())
    .filter(l => l && !isJunk(l));

  let title = '', subtitle = '';
  const activities = [];
  let cur = null;

  for (const line of lines) {
    if (!title) { title = line; continue; }

    // Named activity header: "Activity 1: ..." / "Section A: ..." / "Special Needs..."
    const namedAct = /^(activity\s+[\w]+|section\s+[\w]+|part\s+[\w]+|special needs[\w\s]*)[:\s–\-]*(.*)/i.test(line);
    // Numbered activity header: "1. Circle the words..." — a number followed by a verb/instruction
    const numberedAct = /^\d+\.\s+[A-Z]/.test(line) && line.length > 8;

    if (namedAct || numberedAct) {
      const isSpecial = /special needs/i.test(line);
      cur = { header: line, items: [], special: isSpecial };
      activities.push(cur);
      continue;
    }

    if (!cur) {
      if (!subtitle && line.length < 100) { subtitle = line; continue; }
      cur = { header: '', items: [] };
      activities.push(cur);
    }
    cur.items.push(line);
  }

  // Post-process items
  for (const act of activities) {
    // Strip trailing commas/punctuation from items
    act.items = act.items.map(i => i.replace(/,\s*$/, '').trim()).filter(i => i);

    act.type = detectType(act.header, act.items);

    // If an activity has no items but next sibling has content starting with lowercase/continuation, merge
    // (handled by treating any non-header line as belonging to current activity)

    // Split fill-blank items crammed on one line: "c_t b_g h_t"
    if (act.type === 'fill-blank') {
      const exp = [];
      for (const item of act.items) {
        const multi = item.match(/[a-z]_[a-z]/gi);
        if (multi && multi.length > 1 && item.split(' ').length > 1) {
          exp.push(...item.split(/\s{2,}|,\s*/).filter(p=>p).map(p=>p.replace(/,$/,'')));
        } else { exp.push(item); }
      }
      act.items = exp;
    }

    // Split word-bank items on one line (always)
    if (act.type === 'word-bank') {
      const exp = [];
      for (const item of act.items) {
        if (item.includes(',')) {
          exp.push(...item.split(',').map(w=>w.trim()).filter(w=>w));
        } else if (item.split(/\s{2,}|\t/).length > 1) {
          exp.push(...item.split(/\s{2,}|\t/).map(w=>w.trim()).filter(w=>w));
        } else { exp.push(item); }
      }
      act.items = exp;
    }
  }

  // Remove activities with no items AND no meaningful header
  // Keep headers that have no items (they might just be labels)

  return { title: title||'Worksheet', subtitle: subtitle||'', activities };
}

function detectType(header, items) {
  const h = (header||'').toLowerCase();
  if (/circle|underline|highlight/.test(h)) return 'word-bank';
  if (/match|draw.*line|connect/.test(h)) return 'matching';
  if (/fill.*in|missing|blank/.test(h)) return 'fill-blank';
  if (/write.*word|write.*plural/.test(h)) return 'write-word';
  if (/yes.*no|true.*false/.test(h)) return 'yes-no';
  if (/multiple.*choice|choose.*correct/.test(h)) return 'mcq';
  if (/choose|circle.*correct/.test(h)) {
    return items.some(i=>/^[a-d][).]/i.test(i)) ? 'mcq' : 'word-bank';
  }
  return 'list';
}

// ── Activity header bar ──────────────────────────────────────────────────────
function actHeader(x, y, w, text, special) {
  const fill = special ? C.blue : C.green;
  const pale = special ? C.bluePale : C.greenPale;
  // Strip leading number "1. " from header text
  const clean = text.replace(/^\d+\.\s*/, '');
  const lines = wrap(clean, 80);
  const h = lines.length * 18 + 14;
  let svg = `<rect x="${x}" y="${y}" width="${w}" height="${h}" fill="${pale}" rx="4"/>`;
  svg += `<rect x="${x}" y="${y}" width="5" height="${h}" fill="${fill}" rx="2"/>`;
  for (let i=0;i<lines.length;i++) {
    svg += `<text x="${x+14}" y="${y+14+i*18}" font-family="Arial" font-size="13" font-weight="bold" fill="${fill}">${esc(lines[i])}</text>`;
  }
  return { svg, height: h };
}

// ── Item renderers ────────────────────────────────────────────────────────────

// Generic numbered list — one item per row, neat
function renderList(items, x, y, cw, numbered=true) {
  let svg='', totalH=0;
  const LH = 26; // line height per item
  for (let i=0;i<items.length;i++) {
    const item = items[i];
    const ry = y + totalH;
    const lines = wrap(item.replace(/^\d+\.\s*/,''), 82);
    const rh = lines.length * LH + 4;
    if (i%2===0) svg += `<rect x="${x}" y="${ry}" width="${cw}" height="${rh}" fill="${C.rowAlt}" rx="3"/>`;
    // Number circle
    if (numbered) {
      const numStr = (i+1).toString();
      svg += `<circle cx="${x+14}" cy="${ry+rh/2}" r="10" fill="${C.greenBg}" stroke="${C.greenPale}" stroke-width="1.2"/>`;
      svg += `<text x="${x+14}" y="${ry+rh/2+4}" font-family="Arial" font-size="11" font-weight="bold" fill="${C.greenSoft}" text-anchor="middle">${esc(numStr)}</text>`;
    }
    const tx = numbered ? x+32 : x+10;
    for (let li=0;li<lines.length;li++) {
      svg += `<text x="${tx}" y="${ry+18+li*LH}" font-family="Arial" font-size="13" fill="${C.dark}">${esc(lines[li])}</text>`;
    }
    totalH += rh;
  }
  return { svg, height: totalH+4 };
}

// Fill in the blank — numbered rows with big underline
function renderFillBlank(items, x, y, cw) {
  let svg='', totalH=0;
  const RH = 34;
  for (let i=0;i<items.length;i++) {
    const item = items[i];
    const ry = y + totalH;
    if (i%2===0) svg += `<rect x="${x}" y="${ry}" width="${cw}" height="${RH}" fill="${C.rowAlt}" rx="3"/>`;
    // Number
    svg += `<circle cx="${x+14}" cy="${ry+RH/2}" r="10" fill="${C.greenBg}" stroke="${C.greenPale}" stroke-width="1.2"/>`;
    svg += `<text x="${x+14}" y="${ry+RH/2+4}" font-family="Arial" font-size="11" font-weight="bold" fill="${C.greenSoft}" text-anchor="middle">${i+1}</text>`;

    const clean = item.replace(/^\d+\.\s*/,'').trim();
    const hasBlank = /_{2,}/.test(clean);

    if (hasBlank) {
      const parts = clean.split(/(_{2,})/);
      let cx = x+32;
      for (const part of parts) {
        if (/^_{2,}$/.test(part)) {
          svg += `<line x1="${cx}" y1="${ry+RH-8}" x2="${cx+80}" y2="${ry+RH-8}" stroke="${C.greenSoft}" stroke-width="1.5"/>`;
          cx += 84;
        } else if (part) {
          const w = part.length * 7.5;
          svg += `<text x="${cx}" y="${ry+RH-10}" font-family="Arial" font-size="13" fill="${C.dark}">${esc(part)}</text>`;
          cx += w;
        }
      }
    } else {
      // c_t style — show with underline replacing underscore
      const display = clean.replace(/_/g, '_');
      svg += `<text x="${x+32}" y="${ry+RH-10}" font-family="Arial" font-size="13" fill="${C.dark}">${esc(display)}</text>`;
      svg += `<line x1="${x+cw-140}" y1="${ry+RH-8}" x2="${x+cw-10}" y2="${ry+RH-8}" stroke="${C.greenSoft}" stroke-width="1.5"/>`;
    }
    totalH += RH;
  }
  return { svg, height: totalH+4 };
}

// Word bank — chips in a row
function renderWordBank(items, x, y, cw) {
  let svg='';
  let cx=x, cy=y;
  const CH=28, PAD=12, GAP=8, RG=10, CW_CH=8;
  for (const item of items) {
    const clean = item.replace(/^\d+\.\s*/,'').trim();
    const chipW = clean.length * CW_CH + PAD*2;
    if (cx+chipW > x+cw) { cx=x; cy+=CH+RG; }
    svg += `<rect x="${cx}" y="${cy}" width="${chipW}" height="${CH}" fill="${C.greenBg}" rx="14" stroke="${C.greenPale}" stroke-width="1.5"/>`;
    svg += `<text x="${cx+chipW/2}" y="${cy+19}" font-family="Arial" font-size="12" fill="${C.dark}" text-anchor="middle">${esc(clean)}</text>`;
    cx += chipW+GAP;
  }
  const totalH = (cy-y)+CH+RG;
  return { svg, height: Math.max(totalH, CH+RG) };
}

// Matching — left | dotted | right
function renderMatching(items, x, y, cw) {
  let svg='', totalH=0;
  const RH=32, colW=(cw-50)/2;
  const pairs = items.map(item => {
    const m = item.match(/^(.+?)\s+[-–—|]{1,3}\s+(.+)$/) || item.match(/^(.+?)\s{3,}(.+)$/);
    return m ? [m[1].trim(), m[2].trim()] : [item, ''];
  });
  for (let i=0;i<pairs.length;i++) {
    const ry=y+totalH;
    if (i%2===0) svg += `<rect x="${x}" y="${ry}" width="${cw}" height="${RH}" fill="${C.rowAlt}" rx="3"/>`;
    svg += `<circle cx="${x+14}" cy="${ry+RH/2}" r="10" fill="${C.greenBg}" stroke="${C.greenPale}" stroke-width="1.2"/>`;
    svg += `<text x="${x+14}" y="${ry+RH/2+4}" font-family="Arial" font-size="11" font-weight="bold" fill="${C.greenSoft}" text-anchor="middle">${i+1}</text>`;
    svg += `<text x="${x+32}" y="${ry+21}" font-family="Arial" font-size="13" fill="${C.dark}">${esc(pairs[i][0])}</text>`;
    // Dotted connector
    for (let d=0;d<8;d++) svg += `<circle cx="${x+colW+20+d*8}" cy="${ry+RH/2}" r="1.5" fill="${C.rule}"/>`;
    if (pairs[i][1]) {
      svg += `<text x="${x+colW+90}" y="${ry+21}" font-family="Arial" font-size="13" fill="${C.gray}">${esc(pairs[i][1])}</text>`;
    } else {
      svg += `<line x1="${x+colW+90}" y1="${ry+22}" x2="${x+cw-10}" y2="${ry+22}" stroke="${C.rule}" stroke-width="1"/>`;
    }
    totalH += RH;
  }
  return { svg, height: totalH+4 };
}

// YES/NO rows
function renderYesNo(items, x, y, cw) {
  let svg='', totalH=0;
  const RH=32;
  for (let i=0;i<items.length;i++) {
    const clean = items[i].replace(/yes\s*\/\s*no/i,'').replace(/true\s*\/\s*false/i,'').trim().replace(/^\d+\.\s*/,'');
    const ry=y+totalH;
    if (i%2===0) svg += `<rect x="${x}" y="${ry}" width="${cw}" height="${RH}" fill="${C.rowAlt}" rx="3"/>`;
    svg += `<circle cx="${x+14}" cy="${ry+RH/2}" r="10" fill="${C.greenBg}" stroke="${C.greenPale}" stroke-width="1.2"/>`;
    svg += `<text x="${x+14}" y="${ry+RH/2+4}" font-family="Arial" font-size="11" font-weight="bold" fill="${C.greenSoft}" text-anchor="middle">${i+1}</text>`;
    svg += `<text x="${x+32}" y="${ry+21}" font-family="Arial" font-size="13" fill="${C.dark}">${esc(clean)}</text>`;
    const bx = x+cw-108;
    svg += `<rect x="${bx}" y="${ry+6}" width="46" height="20" fill="${C.white}" rx="4" stroke="${C.rule}" stroke-width="1.2"/>`;
    svg += `<text x="${bx+23}" y="${ry+20}" font-family="Arial" font-size="11" fill="${C.gray}" text-anchor="middle">YES</text>`;
    svg += `<rect x="${bx+52}" y="${ry+6}" width="46" height="20" fill="${C.white}" rx="4" stroke="${C.rule}" stroke-width="1.2"/>`;
    svg += `<text x="${bx+75}" y="${ry+20}" font-family="Arial" font-size="11" fill="${C.gray}" text-anchor="middle">NO</text>`;
    totalH += RH;
  }
  return { svg, height: totalH+4 };
}

// Multiple choice
function renderMCQ(items, x, y, cw) {
  let svg='', totalH=0;
  let qText='', opts=[];

  const flush = () => {
    if (!qText) return;
    const qy=y+totalH;
    const qLines=wrap(qText,85);
    for (let li=0;li<qLines.length;li++) {
      svg += `<text x="${x+10}" y="${qy+li*18+16}" font-family="Arial" font-size="13" font-weight="bold" fill="${C.dark}">${esc(qLines[li])}</text>`;
    }
    totalH += qLines.length*18+4;
    for (const opt of opts) {
      const oy=y+totalH;
      svg += `<circle cx="${x+22}" cy="${oy+10}" r="8" fill="${C.white}" stroke="${C.rule}" stroke-width="1.5"/>`;
      const oLines=wrap(opt,82);
      for (let li=0;li<oLines.length;li++) {
        svg += `<text x="${x+38}" y="${oy+li*17+15}" font-family="Arial" font-size="12" fill="${C.gray}">${esc(oLines[li])}</text>`;
      }
      totalH += oLines.length*17+2;
    }
    totalH += 10;
    qText=''; opts=[];
  };

  for (const item of items) {
    if (/^[a-d][).]/i.test(item)) opts.push(item);
    else { flush(); qText=item; }
  }
  flush();
  return { svg, height: totalH };
}

// Write word rows
function renderWriteWord(items, x, y, cw) {
  let svg='', totalH=0;
  const RH=34;
  for (let i=0;i<items.length;i++) {
    const ry=y+totalH;
    const clean = items[i].replace(/^\d+\.\s*/,'').trim();
    if (i%2===0) svg += `<rect x="${x}" y="${ry}" width="${cw}" height="${RH}" fill="${C.rowAlt}" rx="3"/>`;
    svg += `<circle cx="${x+14}" cy="${ry+RH/2}" r="10" fill="${C.greenBg}" stroke="${C.greenPale}" stroke-width="1.2"/>`;
    svg += `<text x="${x+14}" y="${ry+RH/2+4}" font-family="Arial" font-size="11" font-weight="bold" fill="${C.greenSoft}" text-anchor="middle">${i+1}</text>`;
    svg += `<text x="${x+32}" y="${ry+21}" font-family="Arial" font-size="13" fill="${C.dark}">${esc(clean)}</text>`;
    svg += `<line x1="${x+cw-150}" y1="${ry+RH-8}" x2="${x+cw-10}" y2="${ry+RH-8}" stroke="${C.greenSoft}" stroke-width="1.5"/>`;
    totalH += RH;
  }
  return { svg, height: totalH+4 };
}

// ── Render one activity block ────────────────────────────────────────────────
function renderActivity(act, x, y, cw) {
  let svg='', h=0;

  // Header bar
  if (act.header) {
    const hdr = actHeader(x, y, cw, act.header, act.special);
    svg += hdr.svg; h += hdr.height + 8;
  }

  if (!act.items.length) return { svg, height: h+4 };

  const by = y+h;
  const type = act.special ? 'list' : act.type;
  let body = { svg:'', height:0 };

  switch(type) {
    case 'word-bank':  body = renderWordBank(act.items, x, by, cw); break;
    case 'matching':   body = renderMatching(act.items, x, by, cw); break;
    case 'fill-blank': body = renderFillBlank(act.items, x, by, cw); break;
    case 'yes-no':     body = renderYesNo(act.items, x, by, cw); break;
    case 'mcq':        body = renderMCQ(act.items, x, by, cw); break;
    case 'write-word': body = renderWriteWord(act.items, x, by, cw); break;
    default:           body = renderList(act.items, x, by, cw); break;
  }

  svg += body.svg;
  h += body.height;
  return { svg, height: h + 16 }; // 16px gap after each activity
}

// ── Paginate ─────────────────────────────────────────────────────────────────
function paginate(activities) {
  const pages = [];
  let page = { acts:[], usedH:0, isFirst:true };

  // Available content height per page
  const availFirst = H - HDR1 - (H - FOOT_Y) + 8;   // ~808px
  const availCont  = H - HDR_CONT - (H - FOOT_Y) + 8; // ~865px

  for (const act of activities) {
    const avail = page.isFirst ? availFirst : availCont;
    // Better height estimate based on type
    const headerH = act.header ? 40 : 0;
    const itemH = act.type === 'word-bank' ? 40 :
                  act.type === 'mcq' ? act.items.length * 22 :
                  act.items.length * 32;
    const est = headerH + itemH + 20;

    if (page.usedH + est > avail && page.acts.length > 0) {
      pages.push(page);
      page = { acts:[], usedH:0, isFirst:false };
    }
    page.acts.push(act);
    page.usedH += est;
  }
  if (page.acts.length) pages.push(page);
  return pages;
}

